         public void showTable(List<KhachHang> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tbl_KhachHang_BanHang.getModel();
        defaultTableModel.setRowCount(0);

        for (KhachHang kh : list) {
            Object datarow[] = {
                kh.getId(),
                kh.getHo() + " " + kh.getTendem() + " " + kh.getTen(),
                kh.getGioitinh() == 0 ? "Nam" : "Nữ",
                kh.getNgaysinh(),
                kh.getSdt(),
                kh.getEmail(), 
                kh.getDiemthuong()
            };
            defaultTableModel.addRow(datarow);

        }
    }
         
         public String getIdKH () {
             String idKH = tbl_KhachHang_BanHang.getValueAt(tbl_KhachHang_BanHang.getSelectedRow(), 0)+"";
             return idKH;
         }
