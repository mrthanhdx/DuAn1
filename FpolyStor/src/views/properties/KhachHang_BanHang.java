    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        List<KhachHang> listKH = new KhachHangService_IMPL().getall();
        DefaultTableModel defaultTableModel = (DefaultTableModel) tbl_KhachHang_BanHang.getModel();
        defaultTableModel.setRowCount(0);

        for (KhachHang kh : listKH) {
            if (kh.getSdt().contains(SdtSearchText.getText())) {
                Object datarow[] = {
                    kh.getId(),
                    kh.getHo() + " " + kh.getTendem() + " " + kh.getTen(),
                    kh.getGioitinh() == 0 ? "Nam" : "Nữ",
                    kh.getNgaysinh(),
                    kh.getSdt(),
                    kh.getEmail(),
                    kh.getDiemthuong()
                };
                defaultTableModel.addRow(datarow);
            }

        }
    }//GEN-LAST:event_btnSearchActionPerformed

