    private void loadTableHoaDon(int page, int limit){
        model = (DefaultTableModel) tbl_hoadon.getModel();
        model.setRowCount(0);
        ArrayList<Model.HoaDon1> list = service.getAllHoaDon(page, limit);
        for (Model.HoaDon1 hd : list) {
            model.addRow(new Object[]{
            hd.getMa(),
            hd.getTenNV(),
            hd.getTenKH(),
            hd.getTongTien(),
            hd.getNgayTao(),
            hd.getNgayTT(),
            hd.getTrangThai() ==0?"Chưa Thanh Toán":"Đã Thanh Toán",
            hd.getGhiChu()});
        }
    }
    
