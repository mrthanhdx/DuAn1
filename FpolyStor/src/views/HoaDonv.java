    private void cbo_trangthaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbo_trangthaiActionPerformed
            String timTT = cbo_trangthai.getSelectedItem().toString();
            model = (DefaultTableModel) tbl_hoadon.getModel();
            ArrayList<Model.HoaDon1> list = service.getAllHoaDon(page, limit);
            model.setRowCount(0);
            for (HoaDon1 HoaDon : list) {
                String trangThaiText ="";
                if (HoaDon.getTrangThai()==0) {
                    trangThaiText="Chưa Thanh Toán";
                }else{
                    trangThaiText="Đã Thanh Toán";
                }
                if (trangThaiText.equalsIgnoreCase(timTT)) {
                    model.addRow(new Object[]{
            HoaDon.getMa(),
            HoaDon.getTenNV(),
            HoaDon.getTenKH(),
            HoaDon.getTongTien(),
            HoaDon.getNgayTao(),
            HoaDon.getNgayTT(),
            HoaDon.getTrangThai() ==0?"Chưa Thanh Toán":"Đã Thanh Toán",
            HoaDon.getGhiChu()});
                }
        }
    }//GEN-LAST:event_cbo_trangthaiActionPerformed

    private void txt_tkKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_tkKeyReleased
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        String timTT = txt_tk.getText().trim().toLowerCase();
        ArrayList<Model.HoaDon1> list = service.getAllHoaDon(page, limit);
        model.setRowCount(0);
        for (HoaDon1 hoaDon : list) {
            String tenKH = hoaDon.getTenKH();
            
            if (tenKH != null && tenKH.toLowerCase().contains(timTT)) {
                model.addRow(new Object[]{
                    hoaDon.getMa(),
                    hoaDon.getTenNV(),
                    hoaDon.getTenKH(),
                    hoaDon.getTongTien(),
                    hoaDon.getNgayTao(),
                    hoaDon.getNgayTT(),
                    (hoaDon.getTrangThai() == 0) ? "Chưa Thanh Toán" : "Đã Thanh Toán",
                    hoaDon.getGhiChu()
                });
            }
        }
    }
    }//GEN-LAST:event_txt_tkKeyReleased

