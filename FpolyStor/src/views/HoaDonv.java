    private void tbl_hoadonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_hoadonMouseClicked
        int row = tbl_hoadon.getSelectedRow();
        model = (DefaultTableModel) tbl_HoaDonCT.getModel();
        model.setRowCount(0);
        ArrayList<HoaDonChiTiet> list = service.getAllHoaDonChiTiet(tbl_hoadon.getValueAt(row, 0).toString());
        for (HoaDonChiTiet hdct : list) {
            model.addRow(new Object[]{
                hdct.getMaHoaDon(),
                hdct.getMaSP(),
                hdct.getSoLuong(),
                hdct.getDonGia(),
                hdct.getThanhTien()
            });
        }
    }//GEN-LAST:event_tbl_hoadonMouseClicked

